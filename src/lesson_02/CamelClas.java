package lesson_02;

public class CamelClas {
    public static void main(String[] args) {
        int myFirstVariable;
        myFirstVariable = 1;

        int mySecondVariable;
        mySecondVariable = 2;

        System.out.println("myFirstVariable" + myFirstVariable);

        System.out.println(" mySecondVariable " + mySecondVariable);

        long longVariable = 222_2222_0000L;
        System.out.println(" longVariable " + longVariable);

        double doubleVariable = 1.5665;
        System.out.println(" doubleVariable " + doubleVariable);

    }
}
